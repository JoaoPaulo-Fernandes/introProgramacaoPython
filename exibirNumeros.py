# Programa que exibe os números de 1 a 100. Utilizando o bloco de repetição (While)

x = 1                   # X inicia com o valor de 1
while x <= 100:         # Enquanto a condição for verdadeira, o bloco será executado
    print(x)            # Imprime o valor de x
    x = x + 1           # X recebe o valor de 1 e sempre realiza uma nova soma.
#Programa que divide dois números e exibe o seu valor.

# Divisão com resultado não inteiro.
print(8/2)

print('------------------')

# Divisão um pouco mais elaborada e não inteira. Utilizando variáveis. (Lêr-se "a" recebe 10, "b" recebe 2 e "divisão" recebe a / b)
a = 10
b = 2
divisao = a / b
print(divisao)

print('-----------------')

# Divisão com valor inteiro. Utiliza-se duas (//).
print(8//4)

# Divisão com resto. Utiliza-se o símbolo de porcentagem (%).
print(10 % 3)
print(16 % 7)



# Por exemplo ao queremos armazenar um conteúdo simples como: verdadeiro ou falso, utilizamos a variável do tipo lógico ou booleana (True == verdadeiro) e (False == falso).
# Uma observação: as letras iniciais são maiúsculas (T) e (F).

resultado = True
reprovado = False

print(resultado)
print(reprovado)

nota = 7
print(nota >= 7) # Pergunta se "nota" é igual ou maior a "7".

nota = 5
print(nota != 5) # Pergunta se "nota" é diferente de "5".